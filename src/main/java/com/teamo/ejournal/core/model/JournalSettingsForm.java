package com.teamo.ejournal.core.model;

public class JournalSettingsForm {

	public JournalSettingsForm() {
	}
	private String journalTitle;
	private String aimsAndGoals;
	
	public String getJournalTitle() {
		return journalTitle;
	}
	public void setJournalTitle(String journalTitle) {
		this.journalTitle = journalTitle;
	}
	public String getAimsAndGoals() {
		return aimsAndGoals;
	}
	public void setAimsAndGoals(String aimsAndGoals) {
		this.aimsAndGoals = aimsAndGoals;
	}
	@Override
	public String toString() {
		return "JournalSettings [journalTitle=" + journalTitle
				+ ", aimsAndGoals=" + aimsAndGoals + "]";
	}
	
}
